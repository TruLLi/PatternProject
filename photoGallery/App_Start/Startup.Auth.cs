            //    clientSecret: "");

            //app.UseTwitterAuthentication(
            //   consumerKey: "",
            //   consumerSecret: "");

            //app.UseFacebookAuthentication(
            //   appId: "",
            //   appSecret: "");

            app.UseGoogleAuthentication(new GoogleOAuth2AuthenticationOptions()
            {
                ClientId = "931306208261-o8euuojb837nnh3f59tbvdgtlo7llhtq.apps.googleusercontent.com",
                ClientSecret = "xkBLcPZXb2cyMc3SA7yu5DVk"
            });
            app.UseGitHubAuthentication("6d4716807d82b691aeba", "816125a4b6ae5c46328ba2c3971a2a862195488f");
        }
